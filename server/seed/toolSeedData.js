const generatedTools = [];
const usedNames = new Set(curatedTools.map((tool) => tool.name.toLowerCase()));
const usedSlugs = new Set(curatedTools.map((tool) => tool.slug));

for (const [categoryIndex, category] of broadCategories.entries()) {
  const config = generatedCategoryConfig[category.name];

  if (!config) {
    continue;
  }

  for (let itemIndex = 0; itemIndex < 40 && curatedTools.length + generatedTools.length < targetToolCount; itemIndex += 1) {
    const prefix = generatedNamePrefixes[(categoryIndex + itemIndex) % generatedNamePrefixes.length];
    const stem = config.stems[itemIndex % config.stems.length];
    const suffix = generatedNameSuffixes[(categoryIndex * 3 + itemIndex) % generatedNameSuffixes.length];
    const name = `${prefix} ${stem} ${suffix}`;
    const slug = createSlug(name);

    if (usedNames.has(name.toLowerCase()) || usedSlugs.has(slug)) {
      continue;
    }

    const audience = config.audiences[itemIndex % config.audiences.length];
    const benefit = config.benefits[itemIndex % config.benefits.length];
    const pricing = pricingRotation[(categoryIndex + itemIndex) % pricingRotation.length];
    const description = `${category.name} workspace for ${audience} to ${benefit}.`;
    const websiteUrl = `https://${slug}.ai`;
    const tool = buildToolDocument({
      name,
      category: category.name,
      pricing,
      description,
      websiteUrl,
      index: curatedTools.length + generatedTools.length,
      extraTags: ["Seeded", "Directory"],
    });

    tool.image.url = getAvatarLogoUrl(name);

    generatedTools.push(tool);
    usedNames.add(name.toLowerCase());
    usedSlugs.add(slug);
  }
}

