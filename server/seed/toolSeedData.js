const buildToolDocument = ({ name, category, pricing, description, websiteUrl, index, extraTags = [] }) => {
  const featured = index % 8 === 0 || index % 13 === 0;
  const normalizedCategory = normalizeCategory(category);
  const tags = [normalizedCategory, pricing, featured ? "Featured" : "Trending", "AI Tool", ...extraTags];

  if (normalizedCategory !== category) {
    tags.push(category);
  }

  return {
    name,
    slug: createSlug(name),
    description,
    longDescription: `${description} ${name} helps teams in the ${normalizedCategory.toLowerCase()} category move faster with cleaner workflows, stronger output quality, and scalable AI-powered execution.`,
    category: normalizedCategory,
    pricing,
    featured,
    websiteUrl,
    image: {
      url: websiteUrl ? getLogoSeedUrl(websiteUrl) : getAvatarLogoUrl(name),
      publicId: "",
    },
    tags: [...new Set(tags)].slice(0, 8),
    rating: Number((4.4 + ((index % 6) * 0.1)).toFixed(1)),
    monthlyVisits: `${(index + 3) * 11}K+`,
  };
};

